const S2T = new Map<string, string>(S2T_PAIRS)
const T2S = new Map<string, string>(S2T_PAIRS.map(([s, t]) => [t, s] as [string, string]))

function s2t(text: string): string {
  let result = text
  for (const [simp, trad] of PHRASE_S2T) {
    result = result.split(simp).join(trad)
  }
  result = result.split('').map(c => S2T.get(c) ?? c).join('')
  return result
}

function t2s(text: string): string {
  let result = text
  for (const [simp, trad] of PHRASE_S2T) {
    result = result.split(trad).join(simp)
  }
  result = result.split('').map(c => T2S.get(c) ?? c).join('')
  return result
}

function getDiff(input: string, output: string): { char: string; changed: boolean }[] {
  return output.split('').map((char, i) => ({
    char,
    changed: input[i] !== char && input[i] !== undefined,
  }))
}

